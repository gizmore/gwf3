<p>These hacker camps contain a kitchen, teachers, bedroom and equipment to continue IT Research in poor situations.</p>
</div>

<div id="p11">
<h2>Executive board members</h2>
<p>Executive board members are the members with the highest score for a country.</p>
<p>As there are, at state of writing, 242 countries listed, there is a possible pool of 242 executives.</p>
<p>The best member can choose himself to become executive.</p>
<p>It is allowed to occupy countries where you are not registered. Just keep in mind that double accounts are forbidden and the founders have veto.</p>
