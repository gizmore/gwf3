<div id="p3">
<h2>§3 General assembly</h2>
<p>The members gather automatically and sporadic on any #wechall channel on any IRC network..</p>
<p>For executive members, presence in channels is a must, at least once a week.</p>
<p>Votes and elections are held at the first and fifteenth each month.</p>
<p>Presence at elections and votings is also a must for executive member.</p>
</div>

<div id="p4">
<h2>§4 Membership</h2>
<p>Membership is gained by a configuration on your profile on the main website <a href="https://www.wechall.net">www.wechall.net</a>.</p>
<p>An application cannot be denied, and thus the membership can be chosen automatically.</p>
<p>One natural person may apply for one membership. Violation of this rule means loss of membership of the natural person.</p>
<p>Exit of the membership is also possible via a configuration.</p>
</div>

<div id="p5">
<h2>§5 Voting and weighting</h2>
<p>Every member is allowed to vote. There are <a href="#p7"></a>three types of membership</a>.
<p>The 10 founders may put a veto by simple majority.</p>
<p>The executive members have quadruple weighting of the <a href="#p6">normal weight</a>.</p>
<p></p>
</div>

<div id="p6">
<h2>§6 normal weight</h2>
<p>The weight of a vote equals the score on wechall.net.</p>
<p>The algorithm which is in use may not be altered and works like <a href="https://www.wechall.net/scoring_faq">described and implemented there</a>.</p>
<p>Adaption of the variables have to be accounted for by voting.</p>
</div>

<div id="p7">
<h2>§7 Types of membership</h2>
<p>There are three types of membership with differenciated voting weights.</p>
<ol>
<li>10 founders without special voting weights.</li>
<li>up to 242 Executive Board members with quadrupled voting weights.</li>
<li>normal members without special voting weights.</li>
</ol>
</div>

